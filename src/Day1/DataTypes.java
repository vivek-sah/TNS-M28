package Day1;

public class DataTypes {
    public static void main(String[] args) {
        // types of primitive integer datatypes
        byte a=12;
        short b=13;
        int c=14;
        long d=44444444444444l;
        System.out.println(".......integer datatypes...........");
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);



        // types of primitive floating point datatypes
        System.out.println(".........floating point..........");
        float f=34.5f;
        double g=24577.12346d;
        System.out.println(f);
        System.out.println(g);
        // types of primitive character datatypes
        System.out.println(".........character...........");
        char ch='D';
        System.out.println(ch);

        // types of primitive boolean types of data
        System.out.println("..........boolean..........");
        boolean bh=true;
        System.out.println(bh);




    }
}
